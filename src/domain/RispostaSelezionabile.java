package domain;

public class RispostaSelezionabile {

	private String testo;
	private int ID;
	private int Label;

	public String getTesto() {
		return testo;
	}

	public void setTesto(String testo) {
		this.testo = testo;
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public int getLabel() {
		return Label;
	}

	public void setLabel(int label) {
		Label = label;
	}

}
